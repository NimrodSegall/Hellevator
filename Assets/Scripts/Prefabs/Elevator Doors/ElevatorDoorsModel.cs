
public class ElevatorDoorsModel
{
    public readonly float doorOpenAnimationDuration;
    public readonly float doorCloseAnimationDuration;

    public ElevatorDoorsModel(float doorOpenAnimationDuration, float doorCloseAnimationDuration)
